# Тип для словника з даними банку:
# ключ — ім'я клієнта (str), значення — баланс (float)
BankData = dict[str, float]


def deposit_money(bank_data: BankData) -> None:
    """
    Функція для зарахування грошей на баланс користувача.

    Запитує у користувача ім'я та суму поповнення.
    - Якщо користувача ще немає в словнику, додає його з початковим балансом = сума поповнення.
    - Якщо користувач вже існує, додає суму до наявного балансу.

    :param bank_data: Dict[str, float] - словник, де:
        ключ - ім'я клієнта,
        значення - баланс на рахунку.
    :return: None
    """
    # TODO:
    # 1. Запросити у користувача ім'я (input).
    # 2. Запросити у користувача суму (input), перетворити на float.
    # 3. Перевірити, чи є ім'я в словнику:
    #       - якщо немає — додати нового клієнта із вказаною сумою як балансом;
    #       - якщо є — додати суму до існуючого балансу.
    # 4. Вивести повідомлення про успішне поповнення та поточний баланс.

    name = input("Введіть ім'я клієнта: ")
    amount = float(input("Введіть суму поповнення: "))

    if name not in bank_data:
        bank_data[name] = amount
        print(f"Клієнт {name} новий і доданий в базу! Його баланс: {bank_data[name]}")
    else:
        bank_data[name] += amount
        print(f"Рахунок поповнено! Баланс {name}: {bank_data[name]}")


