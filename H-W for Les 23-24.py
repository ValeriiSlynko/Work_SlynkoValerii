# Завдання 3
# У списку цілих, заповненому випадковими числами, визначити мінімальний, додатний елемент і
# максимальний, від'ємний елемент, порахувати кількість від'ємних елементів, порахувати кількість додатних елементів,
# порахувати кількість нулів. Результати вивести на екран.
import random

ls = [random.randint(-10, 20) for _ in range(10)]  # генеруємо випадкові числа
min_pos = []  #   список мінімальних додатних елементів
max_neg = []  #   список максимальних від'ємних елементів

num_neg = 0  #   лічильник від'ємних елементів
num_pos = 0  #   лічильник додатних елементів
num_zero = 0  #   лічильник 0-ів

for num in ls:
    if num < 0:
        num_neg += 1
    else:
        num_pos += 1
    if num == 0:
        num_zero += 1
    if num > 0:
        min_pos.append(num)
    else:
        max_neg.append(num)

print("Список випадкових чисел: ", ls)
print("Мінімальний додатний елемент: ", min(min_pos))
print("Максимальний від'ємний елемент: ", min(max_neg))
print("Кількість від'ємних чисел: ", num_neg)
print("Кількість додатних чисел: ", num_pos)
print("Кількість нулів: ", num_zero)

# Завдання 4
# Користувач із клавіатури вводить список цілих чисел і деяке число.
# Необхідно видалити зі списку всі елементи, які менші за задане число. Результат вивести на екран.

list_num = input("Введіть список цілих чисел: ")
some_num = int(input("Введіть деяке число: "))

numbers = list(map(int, list_num))  # створюємо змінну-список
res = []
for num in numbers:
    if num >= some_num:
        res.append(num)  # додаємо в результат елемент(в кінець)

print(
    "Числа, які >= заданого числа: ", *sorted(res)
)  # результат із методом розпак.* та сортування

