# Користувач з клавіатури вводить список цілих чисел.
# Необхідно порахувати, скільки у списку парних і непарних чисел. Результати вивести на екран.

user_numbers = input("Введіть список цілих чисел: ")
numbers = list(map(int, user_numbers))  # переводимо рядок у список та цілі числа

evens_num = 0  # лічильник для парних чисел
odds_num = 0  # лічильник для не парних

for number in numbers:
    if number % 2 == 0:
        evens_num += 1
    else:
        odds_num += 1
print("Кількість парних: ", evens_num)
print("Кількість не парних: ", odds_num)

