# Користувач вводить з клавіатури арифметичний вираз. Наприклад, 23+12.
# Необхідно вивести на екран результат виразу. У нашому прикладі це 35.
# Арифметичний вираз може складатися тільки з трьох частин: число, операція, число. Можливі операції: +, -, *, /.

arithmetic_exp = input("Введіть арифметичний вираз(напр.23+12): ")

if "+" in arithmetic_exp:
    a, b = arithmetic_exp.split("+")
    print("Результат виразу = ", float(a) + float(b))
elif "-" in arithmetic_exp:
    a, b = arithmetic_exp.split("-")
    print("Результат виразу = ", float(a) - float(b))
elif "*" in arithmetic_exp:
    a, b = arithmetic_exp.split("*")
    print("Результат виразу = ", float(a) * float(b))
elif "/" in arithmetic_exp:
    a, b = arithmetic_exp.split("/")
    print("Результат виразу = ", float(a) / float(b))
else:
    print("Введено не вірний арифметичний вираз")

# Завдання 6
# Користувач із клавіатури вводить список цілих чисел.
# Необхідно відсортувати цей список так, щоб від'ємні числа залишилися на своїх місцях,
# решта елементів були відсортовані за зростанням. Результат вивести на екран.

list_int = input("Ведіть список цілих чисел: ")
list_int = list(map(int, list_int.split()))

positive_num = []  # створюємо список відокремлених додатних чисел

for num in list_int:
    if num >= 0:
        positive_num.append(num)
positive_num.sort()

sort_num = []  # створюємо список для виводу
element = 0  # лічильник перебраних елементів

for num in list_int:
    if num < 0:
        sort_num.append(num)
    else:
        sort_num.append(positive_num[element])  #
        element += 1
print("Результат відсортованих чисел: ", *sort_num)
