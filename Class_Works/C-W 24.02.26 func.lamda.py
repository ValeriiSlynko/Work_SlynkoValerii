c = float(input("Введіть сторону c: "))
print(f"Периметр трикутника зі сторонами {a}, {b}, {c} дорівнює {perimeter(a, b, c)}")

print("\n с) Отримуємо прізвище та ім'я та повертаємо рядок")
surname = input("Введіть прізвище: ")
