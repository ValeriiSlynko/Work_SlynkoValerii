# Напишіть функцію, яка приймає два числа як параметр і відображає всі парні числа між ними.


def func(n1: int, n2: int):
    step = 1 if n1 < n2 else -1  # створена умова на випадок якщо 1-ше число більше 2-го

    for num in range(n1 + step, n2, step):
        if num % 2 == 0:
            print(f"Парні числа між {n1} та {n2} є: ", num)


func(1, 15)

