# Напишіть функцію, яка відображає порожній або заповнений квадрат з деякого символу.
# Функція приймає як параметри: довжину сторони квадрата, символ і змінну логічного типу:
# якщо вона дорівнює True, квадрат заповнений;
# якщо False, квадрат порожній.

print("Будуємо квадратік")


def square(length, symbol, parameter):
    for sq in range(length):  # преребираємо довжину рядка для буд. квадрата
        if parameter or sq == 0 or sq == length - 1:
            print(symbol * length)
        else:
            print(symbol + " " * (length - 2) + symbol)


square(5, "*", False)  # аргументи для передачі параметрів функції

