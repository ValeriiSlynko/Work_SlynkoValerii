# Напишіть функцію, яка перевіряє чи є число паліндромом. Число передається як параметр.
# Якщо число паліндром, потрібно повернути з функції true, інакше false.
# "Паліндром" — це число, у якого перша частина цифр дорівнює другій перевернутій частині цифр.
# Наприклад, 123321 — паліндром (перша частина 123, друга 321, яка після перевороту стає 123),
# 546645 — паліндром, а 421987 — не паліндром.

number = 1233215  # створюємо змінну для передачі в аргумент функції (можно без неї)


def palindrome(num):
    return (
        str(num) == str(num)[::-1]
    )  # переводимо в строку і через return визн. параметр True або False


check = palindrome(num=number)  # прописуємо аргумент для параметрів функції

print(f"Чи є число {number} паліндромом?")
if check:  #
    print(check, "- Так, це число є Паліндром")
else:
    print(check, "- Ні це число не є паліндром")
