start = int(input("Введіть числовий початок діапазону: "))
finish = int(input("Введіть числовий кінець діапазону: "))
count = start
print("Аналіз чисел діапазону")
while count <= finish:
    if count % 3 == 0 and count % 5 == 0:
        print(f"FizzBuzz (число {count} ділиться на 3 і 5)")
    elif count % 3 == 0:
        print(f"Fizz (число {count} ділиться на 3)")
    elif count % 5 == 0:
        print(f"Buzz (число {count} ділиться на 5)")
    else:
        print(f"Число {count} не ділиться на 3 і 5")
    count += 1

# Завдання 4
# Користувач вводить два числа і крок (інтервал), з яким потрібно проходити по діапазону.
# Програма має показати числа від початку діапазону до кінця, збільшуючи кожне число на вказаний крок.
# Також програма повинна надавати вибір порядку виведення: у прямому або зворотному порядку.
start = int(input("Введіть числовий початок діапазону: "))
finish = int(input("Введіть числовий кінець діапазону: "))
step = int(input("Введіть інтервал: "))
output = input("Порядок виведення (direct or reverse): ")


if output == "direct":
    count = start
    while count <= finish:
        print(count, end=" ")
        count += step
elif output == "reverse":
    count = finish
    while count >= start:
        print(count)
        count -= step
print("\nДіапазон завершений")

