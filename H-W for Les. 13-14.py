# Користувач вводить два числа, що представляють діапазон.
# Програма повинна пройти по діапазону і вивести добуток усіх чисел, які діляться на 4, але не діляться на 6.
# Якщо таких чисел немає, вивести відповідне повідомлення.
# Діапазон має автоматично нормалізуватися, якщо початок більший за кінець.
start = int(input("Введіть числовий початок діапазону: "))
finish = int(input("Введіть числовий кінець діапазону: "))

if start > finish:
    start = finish and finish == start

number = start  # лічильник чисел у діапазоні
calculate = 1  # накопичувальник добутку
finding = False  # індикатор знаходження чисел діапазону

while number <= finish:
    if number % 4 == 0 and number % 6 != 0:
        print("Перелік чисел, які підходять умові: ", number)
        calculate *= number
        finding = True
    number += 1
if finding:
    print("Добуток чисел які діляться на 4, але не діляться на 6 =", calculate)
else:
    print("В діапазоні відсутні числа, які діляться на 4, але не діляться на 6")

# Завдання 6
# Користувач вводить два числа: число A і ступінь N, у який потрібно піднести число.
# Програма повинна обчислити A у степені N за допомогою циклу (без використання вбудованих функцій піднесення до степеня).
A = int(input("Введіть число: "))
degree = int(input("Ступінь піднесення да числа: "))
variable = 1  # змінна для використання як степень (оминаючи вбуд.функцію **)
count = 0  # лічильник
while count < degree:
    variable = variable * A
    count += 1
print(f"Число {A} в {degree} степені = {variable}")
