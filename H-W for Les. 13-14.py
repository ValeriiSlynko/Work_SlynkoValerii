print("Усі числа діапазону в зростаючому порядку:")
while counter <= finish:
    print(counter)
    counter += 1

counter = finish
print("Усі числа діапазону в спадному порядку:")
while counter >= start:
    print(counter)
    counter -= 1

counter = start  # Лічильник чисел у діапазоні
