# Напишіть функцію з параметром show_time, яка запитує в користувача ім’я та повертає його.
# Якщо show_time = True, то функція повинна вивести на екран час своєї роботи у секундах.
# Див time.time()

import time


def useer_name(show_time: bool = True) -> str:  # функція з bool-параметром "show_time"
    # Опис документації щодо роботи даної функції
    """
    Запитує користувача ім'я та повертає його
    Якщо show_time = True, то функція виводить час своєї роботи (у секундах)
    Логіка виконання:
    - використовуємо бібліотеку time.time()
    - зробити умову для параметра show_time
    - використовуємо цикл while
    :param show_time: boolean
    :return: str (повертає Ім'я та час роботи)
    """  # docstring (документація рядка)

    start_time = time.time()  # початок роботи функції (написання ім'я)
    while True:
        name = str(input("Введіть своє їм'я: "))  # запитуємо ім'я користувача
        if name != "":  # умова, якщо порожній рядок
            break
        print("Введене ім'я не може бути порожнім!")

    end_time = (
        time.time()
    )  # кінець роботи функції (натискання ENTER після написання ім'я)
    function_time = (
        end_time - start_time
    )  # формула обрахунку тривалості роботи функції (тобто написання ім'я)

    if show_time:
        print(f"Час роботи функції після вводу імені склав - {function_time:.3f} сек.")
    return name


if __name__ == "__main__":  # для визначення точки входу програми
    name = useer_name(show_time=True)
    print(f"Привіт, шановний {name}!")
